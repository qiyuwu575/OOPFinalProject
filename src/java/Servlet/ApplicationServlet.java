package Servlet;

import java.io.IOException;
import java.util.List;
import javax.servlet.ServletException;
import javax.servlet.annotation.WebServlet;
import javax.servlet.http.HttpServlet;
import javax.servlet.http.HttpServletRequest;
import javax.servlet.http.HttpServletResponse;
import javax.servlet.http.HttpSession;
import model.User;
import DAO.ApplicationDAO;
/**
 *
 * @author qiyuw
 */
@WebServlet("/professional/applications")  // 确保这个路径与链接匹配
public class ApplicationServlet extends HttpServlet {
    private ApplicationDAO applicationDAO = new ApplicationDAO();
    
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        HttpSession session = request.getSession(false);
        if (session == null || session.getAttribute("user") == null) {
            response.sendRedirect(request.getContextPath() + "/login.jsp");
            return;
        }
        
        User user = (User) session.getAttribute("user");
        List<model.Application> applications = applicationDAO.getApplicationsByProfessional(user.getUserId());
        request.setAttribute("applications", applications);
        request.getRequestDispatcher("/professional/application.jsp").forward(request, response);
    }

    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        doGet(request, response);
    }
}